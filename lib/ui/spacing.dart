/// App Spacing Constants
/// Consistent spacing values used throughout the app
class AppSpacing {
  // Base spacing unit (8dp)
  static const double base = 8.0;
  
  // Common spacing values
  static const double xs = base * 0.5; // 4dp
  static const double sm = base; // 8dp
  static const double md = base * 2; // 16dp
  static const double lg = base * 3; // 24dp
  static const double xl = base * 4; // 32dp
  static const double xxl = base * 5; // 40dp
  static const double xxxl = base * 6; // 48dp
  
  // Specific use cases
  static const double cardPadding = md; // 16dp
  static const double screenPadding = md; // 16dp
  static const double sectionSpacing = lg; // 24dp
  static const double componentSpacing = md; // 16dp
  static const double iconSize = lg; // 24dp
  static const double iconSizeLarge = xl; // 32dp
  
  // Border radius
  static const double radiusXs = 4.0;
  static const double radiusSm = 8.0;
  static const double radiusMd = 12.0;
  static const double radiusLg = 16.0;
  static const double radiusXl = 20.0;
  static const double radiusRound = 999.0; // Fully rounded
  
  // Elevation
  static const double elevationLow = 2.0;
  static const double elevationMedium = 4.0;
  static const double elevationHigh = 8.0;
  static const double elevationModal = 16.0;
}
